def add(a: float, b: float) -> float:
    """Dodaje dwie liczby"""
    return a + b


def subtract(x: float, y: float) -> float:
    """Odejmuje drugą liczbę od pierwszej"""
    return x - y


def multiply(x: float, y: float) -> float:
    """Mnoży dwie liczby"""
    return x * y


def divide(x: float, y: float) -> float:
    """Dzieli pierwszą liczbę przez drugą"""
    return x / y